#!/usr/bin/env node
/**
 * generate-skill-pr-comment.js
 *
 * Reads a SKILL.md from a skill directory and generates an ASCII review
 * as a PR comment. Used by the curate-on-pr GitHub Action.
 *
 * Usage:
 *   node scripts/generate-skill-pr-comment.js skills/rebuild-plugins-before-deploy
 */

const fs = require('fs');
const path = require('path');

function parseFrontmatter(content) {
  content = content.replace(/\r\n/g, '\n').replace(/\r/g, '\n');
  const match = content.match(/^---\n([\s\S]*?)\n---\n([\s\S]*)$/);
  if (!match) return { meta: {}, body: content };

  const meta = {};
  const lines = match[1].split('\n');

  for (const line of lines) {
    const kvMatch = line.match(/^(\w[\w_-]*):\s*(.*)$/);
    if (kvMatch) {
      let val = kvMatch[2].trim();
      if (val.startsWith('[') && val.endsWith(']')) {
        val = val.slice(1, -1).split(',').map(s => s.trim());
      }
      if (val === '' || val === '>') continue;
      meta[kvMatch[1]] = val;
    }
    const nestedMatch = line.match(/^\s+(\w[\w_-]*):\s*(.+)$/);
    if (nestedMatch) {
      meta[nestedMatch[1]] = nestedMatch[2].trim();
    }
  }
  return { meta, body: match[2] };
}

function extractSections(body) {
  const sections = {};
  let currentSection = null;
  let currentContent = [];

  for (const line of body.split('\n')) {
    const headingMatch = line.match(/^##\s+(.+)$/);
    if (headingMatch) {
      if (currentSection) {
        sections[currentSection] = currentContent.join('\n').trim();
      }
      currentSection = headingMatch[1].trim().toLowerCase();
      currentContent = [];
    } else if (currentSection) {
      currentContent.push(line);
    }
  }
  if (currentSection) {
    sections[currentSection] = currentContent.join('\n').trim();
  }
  return sections;
}

function extractListItems(text) {
  if (!text) return [];
  const items = [];
  for (const line of text.split('\n')) {
    const m = line.match(/^[-*]\s+(.+)$/);
    if (m) items.push(m[1].trim());
    const n = line.match(/^\d+\.\s+(.+)$/);
    if (n) items.push(n[1].trim());
  }
  return items;
}

function generateSkillComment(skillDir) {
  const skillPath = path.join(skillDir, 'SKILL.md');
  if (!fs.existsSync(skillPath)) {
    console.error(`Error: ${skillPath} not found`);
    process.exit(1);
  }

  const content = fs.readFileSync(skillPath, 'utf-8');
  const { meta, body } = parseFrontmatter(content);
  const sections = extractSections(body);

  const name = meta.name || path.basename(skillDir);
  const version = meta.version || '1.0.0';
  const description = meta.description || '(no description)';
  const tags = Array.isArray(meta.tags) ? meta.tags : [];

  let output = [];

  output.push(`## Skill Review: ${name}`);
  output.push('');
  output.push(`**Version:** ${version}`);

  if (tags.length > 0) {
    output.push(`**Tags:** ${tags.map(t => `\`${t}\``).join(' ')}`);
  }

  output.push('');
  output.push(`> ${description}`);
  output.push('');

  if (sections.triggers) {
    output.push('### Triggers');
    output.push('```');
    const triggers = extractListItems(sections.triggers);
    if (triggers.length > 0) {
      triggers.forEach(t => output.push(`  - ${t}`));
    } else {
      output.push(sections.triggers.split('\n').map(l => `  ${l}`).join('\n'));
    }
    output.push('```');
    output.push('');
  }

  if (sections['errors prevented']) {
    output.push('### Errors Prevented');
    output.push('');
    const errorText = sections['errors prevented'];
    const errorBlocks = errorText.split(/(?=- \*\*)/);
    for (const block of errorBlocks) {
      const trimmed = block.trim();
      if (trimmed) {
        output.push(trimmed.startsWith('-') ? trimmed : `- ${trimmed}`);
      }
    }
    output.push('');
  }

  output.push('---');
  output.push('*Generated by [d9 SkillOps](https://github.com/LaWebcapsule/d9-skills)*');

  return output.join('\n');
}

const skillDir = process.argv[2];
if (!skillDir) {
  console.error('Usage: node generate-skill-pr-comment.js <skill-directory>');
  process.exit(1);
}

console.log(generateSkillComment(path.resolve(skillDir)));
